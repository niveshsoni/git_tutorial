def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

if __name__ == "__main__":
    print("Simple Calculator")
    print("1 + 2 =", add(1, 2))
    print("5 - 3 =", subtract(5, 3))

def multiply(a, b):
    return a * b

if __name__ == "__main__":
    print("3 * 4 =", multiply(3, 4))

def square(a):
    return a * a

if __name__ == "__main__":
    print("5 squared =", square(5))
